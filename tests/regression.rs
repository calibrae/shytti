// --- Session ID uniqueness ---

#[test]
fn session_ids_contain_timestamp() {
    // Bug: counter reset on restart could produce duplicate IDs if PID matched.
    // Fixed: build #8 — added epoch timestamp to IDs.
    let mgr = ShellManager::new();
    // IDs are generated by next_id() internally — we can't call it directly,
    // but we can verify spawned shells have unique, timestamp-bearing IDs.
    let rt = tokio::runtime::Runtime::new().unwrap();
    let id1 = rt.block_on(async {
        mgr.spawn(SpawnRequest {
            name: Some("ts-test-1".into()),
            shell: None, cwd: None, host: None, agent: None,
            cmd: Some("sleep 1".into()),
        }).await.unwrap()
    });
    let id2 = rt.block_on(async {
        mgr.spawn(SpawnRequest {
            name: Some("ts-test-2".into()),
            shell: None, cwd: None, host: None, agent: None,
            cmd: Some("sleep 1".into()),
        }).await.unwrap()
    });

    assert_ne!(id1, id2);
    // Format: {pid_hex}-{epoch_hex}-{counter}
    assert_eq!(id1.matches('-').count(), 2, "ID should have 3 segments: {id1}");
    assert_eq!(id2.matches('-').count(), 2, "ID should have 3 segments: {id2}");

    // Clean up
    rt.block_on(async { mgr.kill(&id1).await.ok(); mgr.kill(&id2).await.ok(); });
}

// --- Default shell wiring ---

#[tokio::test]
async fn manager_uses_config_default_shell() {
    // Bug: config default shell was parsed but never used in spawn.
    // ShellManager hardcoded /bin/zsh as fallback.
    // Fixed: build #17 — with_default_shell threads config through.
    let mgr = ShellManager::with_default_shell("/bin/sh".into());

    // Spawn with shell=None — should use the configured default, not hardcoded /bin/zsh
    let id = mgr.spawn(SpawnRequest {
        name: Some("default-shell-test".into()),
        shell: None,
        cwd: None, host: None, agent: None, cmd: None,
    }).await.unwrap();

    let shells = mgr.list().await;
    let shell = shells.iter().find(|s| s.id == id).unwrap();
    assert_eq!(shell.shell_type, shytti::shell::ShellType::Local);

    mgr.kill(&id).await.ok();
}

#[tokio::test]
async fn explicit_shell_overrides_default() {
    // When spawn request specifies a shell, it should override the config default.
    let mgr = ShellManager::with_default_shell("/bin/sh".into());

    let id = mgr.spawn(SpawnRequest {
        name: Some("explicit-shell-test".into()),
        shell: Some("/bin/zsh".into()),
        cwd: None, host: None, agent: None, cmd: None,
    }).await.unwrap();

    let shells = mgr.list().await;
    assert!(shells.iter().any(|s| s.id == id));

    mgr.kill(&id).await.ok();
}

// --- Session recovery ---

#[tokio::test]
async fn session_id_set_and_get() {
    // Supports list_shells: session_id must be stored and retrievable.
    let mgr = ShellManager::new();
    let id = mgr.spawn(SpawnRequest {
        name: Some("session-test".into()),
        shell: None, cwd: None, host: None, agent: None,
        cmd: Some("sleep 60".into()),
    }).await.unwrap();

    assert!(mgr.get_session_id(&id).await.is_none());

    mgr.set_session_id(&id, "sess-abc").await;
    assert_eq!(mgr.get_session_id(&id).await, Some("sess-abc".into()));

    // Reverse lookup
    assert_eq!(mgr.shell_id_by_session("sess-abc").await, Some(id.clone()));
    assert_eq!(mgr.shell_id_by_session("nonexistent").await, None);

    mgr.kill(&id).await.ok();
}

