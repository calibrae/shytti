// --- Control protocol ---

#[test]
fn control_msg_list_shells_roundtrip() {
    // list_shells must serialize/deserialize correctly
    let msg = r#"{"type":"list_shells"}"#;
    let parsed: Result<control::ControlMsg, _> = serde_json::from_str(msg);
    assert!(parsed.is_ok(), "list_shells should parse: {:?}", parsed.err());
    assert!(matches!(parsed.unwrap(), control::ControlMsg::ListShells));
}

#[test]
fn control_msg_shells_list_roundtrip() {
    let msg = control::ControlMsg::ShellsList {
        shells: vec![
            control::ShellListEntry {
                shell_id: "abc".into(),
                session_id: "sess-1".into(),
            },
            control::ShellListEntry {
                shell_id: "def".into(),
                session_id: "sess-2".into(),
            },
        ],
    };
    let json = serde_json::to_string(&msg).unwrap();
    assert!(json.contains("\"type\":\"shells_list\""));
    assert!(json.contains("\"shell_id\":\"abc\""));

    let parsed: control::ControlMsg = serde_json::from_str(&json).unwrap();
    match parsed {
        control::ControlMsg::ShellsList { shells } => {
            assert_eq!(shells.len(), 2);
            assert_eq!(shells[0].shell_id, "abc");
            assert_eq!(shells[1].session_id, "sess-2");
        }
        _ => panic!("expected ShellsList"),
    }
}

